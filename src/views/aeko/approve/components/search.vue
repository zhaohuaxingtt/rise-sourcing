<template>
  <iSearch
    class="margin-bottom20"
    @sure="sure"
    @reset="reset"
    :resetKey="QUEREN"
    :searchKey="REST"
  >
    <el-form>
      <!-- AEKO号 -->
      <el-form-item :label="language('LK_AEKOHAO', 'AEKO号')">
        <iInput
          v-model="form.aekoCode"
          :placeholder="language('LK_QINGSHURU','请输入')"
          clearable
        ></iInput>
      </el-form-item>
      <!-- 零件号 -->
      <el-form-item :label="language('LINGJIAHAO', '零件号')">
        <iInput
          v-model="form.partNum"
          :placeholder="language('LK_QINGSHURU','请输入')"
          clearable
        ></iInput>
      </el-form-item>
      <!-- 科室 -->
      <el-form-item :label="language('LK_AEKOKESHI','科室')">
        <iSelect
          v-model="form.linieDeptNumList"
          :placeholder="language('LK_QINGXUANZE','请选择')"
          filterable
          clearable
        >
          <el-option
            value=""
            :label="language('all','全部') | capitalizeFilter"
          ></el-option>
          <el-option
            :value="items.code"
            :label="items.value"
            v-for="(items, index) in selectOptions.linieDeptNumList || []"
            :key="index"
          ></el-option>
        </iSelect>
      </el-form-item>
      <!-- 专业采购员 -->
      <el-form-item :label="language('ZHUANYECAIGOUYUAN','专业采购员')">
        <iSelect
          v-model="form.linie"
          :placeholder="language('LK_QINGXUANZE','请选择')"
          filterable
          clearable
        >
          <el-option
            value=""
            :label="language('all','全部') | capitalizeFilter"
          ></el-option>
          <el-option
            :value="items.code"
            :label="items.value"
            v-for="(items, index) in selectOptions.buyerName || []"
            :key="index"
          ></el-option>
        </iSelect>
      </el-form-item>
      <!-- CSF股长 -->
      <el-form-item :label="language('CSFGUZHANG','CSF股长')">
        <iSelect
          v-model="form.linie"
          :placeholder="language('LK_QINGXUANZE','请选择')"
          filterable
          clearable
        >
          <el-option
            value=""
            :label="language('all','全部') | capitalizeFilter"
          ></el-option>
          <el-option
            :value="items.code"
            :label="items.value"
            v-for="(items, index) in []"
            :key="index"
          ></el-option>
        </iSelect>
      </el-form-item>
    </el-form>
  </iSearch>
</template>

<script>

import {
  iSearch,
  iInput,
  iSelect,
  iMessage
} from "rise";
import {
  searchCommodity,
  searchLinie,
} from '@/api/aeko/manage'
import {user as configUser } from '@/config'

export default {
  data() {
    return {
      form: {},
      selectOptions: {}
    }
  },
  components: {
    iSearch,
    iInput,
    iSelect,
  },
  mounted() {
    // 获取下拉数据
    this.getSearchList()
  },
  beforeDestroy() {
  },
  methods: {
    sure() {
      this.$emit('search', this.form)
    },
    reset() {
      this.form = {}
      this.$emit('search', {})
    },
    // 获取搜索框下拉数据
    getSearchList(){

      // 科室
      searchCommodity().then((res)=>{
        const {code,data} = res;
        if(code ==200 ){
          data.map((item)=>{
            item.value = item.deptNum;
            item.code = item.id;
          })
          this.selectOptions.linieDeptNumList = data;
        }else{
          iMessage.error(this.$i18n.locale === "zh" ? res.desZh : res.desEn);
        }
      })

      // LINIE
      searchLinie({tagId: configUser.LINLIE}).then((res)=>{
        const {code,data} = res;
        if(code ==200 ){
          data.map((item)=>{
            item.value = this.$i18n.locale === "zh" ? item.nameZh : item.nameEn;
            item.code = this.$i18n.locale === "zh" ? item.nameZh : item.nameEn;
            item.lowerCaseLabel =  typeof item.nameEn === "string" ? item.nameEn.toLowerCase() : item.nameEn
          })
          this.selectOptions.buyerName = data;
        }else{
          iMessage.error(this.$i18n.locale === "zh" ? res.desZh : res.desEn);
        }
      })
    },
  },
}
</script>

