        // 成本结构
        case 'CHENGBENZUCHENG':
          this.getCostData({
            categoryCode: '111'
          }).then(type => {
            if(type == 1) {
              //跳转系统
              this.$router.push({
                path: item.url,
                query: item.params || null
              })
            } else {
              //跳转手工
