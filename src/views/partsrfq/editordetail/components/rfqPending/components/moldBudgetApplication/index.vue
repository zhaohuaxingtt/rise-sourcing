<template>
  <div>
    <iCard v-permission.auto="PARTSRFQ_EDITORDETAIL_RFQPENDING_MOLDBUDGETAPPLICATION_INDEXPAGE|模具预算申请页面">
      <div class="margin-bottom20 clearFloat">
        <div class="floatright" v-if="!disabled">
          <iButton @click="submit" v-permission.auto="PARTSRFQ_EDITORDETAIL_RFQPENDING_MOLDBUDGETAPPLICATION_SUBMIT|模具预算申请提交">{{ language('LK_TIJIAO','提交') }}</iButton>
          <iButton @click="recall" v-permission.auto="PARTSRFQ_EDITORDETAIL_RFQPENDING_MOLDBUDGETAPPLICATION_RECALL|模具预算申请撤回">{{ language('LK_CHEHUI','撤回') }}</iButton>
        </div>
      </div>
      <tableList
        index
        :tableData="tableListData"
        :tableTitle="tableTitle"
        :tableLoading="tableLoading"
        @handleSelectionChange="handleSelectionChange"
        v-permission.auto="PARTSRFQ_EDITORDETAIL_RFQPENDING_MOLDBUDGETAPPLICATION_TABLE|模具预算申请表格"
      >
        <template #budget="scope">
          <iInput v-model="scope.row.budget" v-if="!disabled" @input="handleInput($event, scope.row)" @blur="handleBlurByBudget(scope.row.budget, scope.row)" />
          <span v-else>{{ scope.row.budget }}</span>
        </template>
      </tableList>
      <!------------------------------------------------------------------------>
      <!--                  表格分页                                          --->
      <!------------------------------------------------------------------------>
      <iPagination
          v-update
          @size-change="handleSizeChange($event, getTableList)"
          @current-change="handleCurrentChange($event, getTableList)"
          background
          :page-sizes="page.pageSizes"
          :page-size="page.pageSize"
          :layout="page.layout"
          :current-page='page.currPage'
          :total="page.totalCount"
      />
    </iCard>
  </div>
</template>

<script>
import {iCard, iButton, iPagination, iMessage, iInput} from 'rise';
import {tableTitle} from "./components/data";
import {pageMixins} from "@/utils/pageMixins";
import {getModelBudgetList, patchMouldBudget, cancelMoldBudget} from "@/api/partsrfq/editordetail";
import store from '@/store'
import {rfqCommonFunMixins} from "pages/partsrfq/components/commonFun";
import tableList from "@/views/partsign/editordetail/components/tableList"
import { numberProcessor } from "@/utils"

export default {
  components: {
    iCard,
    iButton,
    iPagination,
    tableList,
    iInput
  },
  mixins: [pageMixins, rfqCommonFunMixins],
  data() {
    return {
      tableListData: [],
      tableTitle: tableTitle,
      tableLoading: false,
      selectTableData: []
    };
  },
  created() {
    this.getTableList();
  },
  inject: ["getDisabled"],
  computed: {
    disabled() {
      return this.getDisabled()
    }
  },
  methods: {
    //获取表格数据
    async getTableList() {
      const id = this.$route.query.id
      if (id) {
        this.tableLoading = true;
        try {
          const req = {
            rfqId: id,
            userId: store.state.permission.userInfo.id
          }
          const res = await getModelBudgetList({
            currPage: this.page.currPage,
            pageSize: this.page.pageSize
          },
          [{ rfqIds: id }]
          )
          this.tableListData = Array.isArray(res.data.records) ? res.data.records.map(item => ({ ...item, budget: math.bignumber(item.budget || 0).toFixed(2) })) : []
          // this.page.currPage = res.current
          // this.page.pageSize = res.size
          this.page.totalCount = res.data.total
          this.tableLoading = false;
        } catch {
          this.tableLoading = false;
        }
      }
    },
    async submit() {
      if(this.selectTableData.length == 0) {
        iMessage.warn(this.language('partsignLanguage.QingXuanZe','请选择'))
        return false
      }

      if (this.selectTableData.some(item => item.approvalStatus === "已提交" || item.approvalStatus === "submitted"))
        return iMessage.warn(this.language("QINGWUXUANZEYITIJIAODESHUJU", "请勿选择已提交的数据"))

      //this.selectTableData = this.selectTableData.map(item => {
        //item.approvalStatus = 'submitted' //后台晓伟指出不转换
        //return item
      //})
      const req = this.selectTableData
      const res = await patchMouldBudget({
        updateType: 1,
        mouldBudgetDTOS: req
      })
      this.resultMessage(res)
      this.getTableList()
    },
    async recall() {
      if(this.selectTableData.length == 0) {
        iMessage.warn(this.language('partsignLanguage.QingXuanZe','请选择'))
        return false
      }

      if (this.selectTableData.some(item => item.approvalStatus === "已撤销" || item.approvalStatus === "revoked"))
        return iMessage.warn(this.language("QINGWUXUANZEYICHEXIAODESHUJU", "请勿选择已撤销的数据"))

     // this.selectTableData = this.selectTableData.map(item => {
        // item.approvalStatus = 'revoked' 晓伟说不用转换
       // return item
     // })
      const req = this.selectTableData
      const res = await patchMouldBudget({
        updateType: 0,
        mouldBudgetDTOS: req
      })
      this.resultMessage(res)
      this.getTableList()
    },
    //修改表格改动列
    handleSelectionChange(val) {
      this.selectTableData = val;
    },
    handleInput(value, row) {
      this.$set(row, "budget", numberProcessor(value, 2))
    },
    handleBlurByBudget(val, row) {
      this.$set(row, "budget", math.bignumber(val || 0).toFixed(2))
    },
  }
}
</script>

